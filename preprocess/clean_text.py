import re


def clean_text(text: str) -> str:
    """
    对原始文本做最基础的清洗。

    这个函数的目标很简单：
    让后续分词和建模看到的输入尽量统一，减少无关噪声。

    参数:
        text: 原始评论文本，例如 "老师讲课很好，但是作业太多!!!"

    返回:
        只保留中文后的字符串，例如 "老师讲课很好但是作业太多"

    说明:
        1. 这里没有做复杂的语义处理，只做最基础的字符清洗。
        2. 当前版本会删除英文、数字和标点，所以像 "PPT"、"95分" 这类信息会被去掉。
        3. 这样做的好处是流程简单、噪声更少，缺点是会损失一部分信息。
    """
    if not isinstance(text, str):
        # 防御式写法:
        # 如果传入的不是字符串，就直接返回空字符串，
        # 避免后续正则处理时报错。
        return ""
    # 正则含义:
    # [^\u4e00-\u9fff]
    # 1. \u4e00-\u9fff 表示常见中文字符范围
    # 2. ^ 放在 [] 内部表示“取反”
    # 3. 因此整个表达式的意思是: 匹配所有“非中文字符”
    #
    # re.sub(..., "", text) 的意思是:
    # 把匹配到的非中文字符全部替换为空字符串。
    return re.sub(r"[^\u4e00-\u9fff]", "", text)


if __name__ == "__main__":
    sample = "老师讲课很好，但是作业太多!!!"
    print(clean_text(sample))
